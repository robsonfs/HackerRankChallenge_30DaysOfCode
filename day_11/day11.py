def get_hourglasses(a):
    arr = []
    for i in range(len(a) - 2):
        for j in range(len(a) - 2):
            hourglass = [
                a[i][j], a[i][j+1], a[i][j+2],
                a[i+1][j+1],
                a[i+2][j], a[i+2][j+1], a[i+2][j+2],
            ]
            arr.append(hourglass)

    return arr

def get_max_sum(hourglasses):
    return max(sum(hourglass) for hourglass in hourglasses)


